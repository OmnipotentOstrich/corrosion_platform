#!/bin/bash

# 防腐保温智能数字平台停止脚本

echo "=========================================="
echo "防腐保温智能数字平台停止脚本"
echo "=========================================="

# 停止所有服务
echo "停止所有服务..."
docker-compose down

# 清理未使用的镜像和容器
echo "清理未使用的镜像和容器..."
docker system prune -f

echo "=========================================="
echo "服务已停止"
echo "=========================================="
echo "如需重新启动，请运行: ./start.sh"
echo "=========================================="
